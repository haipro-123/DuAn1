    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = this.tblKH.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng muốn sửa.");
            return;
        }
        QLKhachHang qlkh = getFormInput();
        JOptionPane.showMessageDialog(this, khachHangService.updateKH(qlkh));
        loadTable(khachHangService.getKH());
        clearForm();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKHMouseClicked
        // TODO add your handling code here:
        int row = this.tblKH.getSelectedRow();
        if (row == -1) {
            return;
        }
        String ma = this.tblKH.getValueAt(row, 0).toString();
        String hoTen = this.tblKH.getValueAt(row, 1).toString();
        String sdt = this.tblKH.getValueAt(row, 2).toString();
        String diaChi = this.tblKH.getValueAt(row, 3).toString();

        this.txtMa.setText(ma);
        this.txtHoTen.setText(hoTen);
        this.txtSdt.setText(sdt);
        this.txtDiaChi.setText(diaChi);
    }//GEN-LAST:event_tblKHMouseClicked

    private void txtTimKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKeyReleased
        // TODO add your handling code here:
         List<QLKhachHang> list = new ArrayList<>();
        for(QLKhachHang x : khachHangService.getKH()){
            if(x.getSdt().contains(this.txtTim.getText())){
                list.add(x);
                System.out.println(txtTim);
            }
        }
        System.out.println(list);
        loadTable(list);
    }//GEN-LAST:event_txtTimKeyReleased
