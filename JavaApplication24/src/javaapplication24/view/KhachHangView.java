    private void loadTable(List<QLKhachHang> list) {
        List<QLKhachHang> ds = list;
        DefaultTableModel dtm = (DefaultTableModel) this.tblKH.getModel();
        dtm.setRowCount(0);
        for (QLKhachHang kh : ds) {
            Object[] rowData = {
                kh.getMaKH(),
                kh.getHoTen(),
                kh.getSdt(),
                kh.getDiaChi()
            };
            dtm.addRow(rowData);
        }

    }

    private QLKhachHang getFormInput() {
        QLKhachHang qlkh = new QLKhachHang();
        String ma = this.txtMa.getText();
        qlkh.setMaKH(ma);

        String hoTen = this.txtHoTen.getText();
        qlkh.setHoTen(hoTen);

        String sdt = this.txtSdt.getText();
        qlkh.setSdt(sdt);

        String diaChi = this.txtDiaChi.getText();
        qlkh.setDiaChi(diaChi);

        return qlkh;
    }

